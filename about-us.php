                        The other half of the merger, Orchid Chess Club, also
                        started in a similar manner following the regular coming
                        together of chess playing friends at Orchid Hotels, Lekki
                        since 2012. The players were all hosted by Engr.
                        Jonathan Odega, a chess enthusiast, successful
                        entrepreneur and Chairman/CEO of Orchid Hotels
                        Limited. After a few years of playing together informally,
                        the friends formed a chess club meant to serve as a
                        hub for like-minded chess players and an arm of Orchid
                        Hotels Limited. Orchid Chess Club went on to organize
                        various chess tournaments before the collaboration.
                        In mid-2019, the leaders of the two clubs began to meet
                        and discuss a potential merger that became a reality
                        some months later.
